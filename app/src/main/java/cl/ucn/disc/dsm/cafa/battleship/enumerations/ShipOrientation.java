/**
 * La orientacion de la nave.
 */
public enum ShipOrientation {
    /**
     * Nave dispuesta verticalmente, usando las celdas de la misma columna.
     */
    VERTICAL,

    /**
     * Nave dispuesta horizontalmente, usando las celdas de la misma fila.
     */
    HORIZONTAL
}